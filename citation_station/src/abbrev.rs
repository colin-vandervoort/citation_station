#[cfg(test)]
mod tests {
    use crate::abbrev::ieee_abbrev;

    #[test]
    fn test_passthrough() {
        let orig = "Thingamabob";
        assert_eq!(ieee_abbrev(orig), orig);
    }

    #[test]
    fn test_combined_arms() {
        // cSpell: disable-next-line
        assert_eq!(ieee_abbrev("Computational"), "Comput.");
        // cSpell: disable-next-line
        assert_eq!(ieee_abbrev("Computer"), "Comput.");
        // cSpell: disable-next-line
        assert_eq!(ieee_abbrev("Computing"), "Comput.");
    }
}
